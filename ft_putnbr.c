#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	digit;

	if (n < 0)
	{
		write(1, "-", 1);
		if (n == -2147483648)
			return (write(1, "2147483648", 10));
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	digit = (n % 10) + '0';
	return (write(1, &digit, 1));
}
